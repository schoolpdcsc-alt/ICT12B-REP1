    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
       int selectedRow = Book.getSelectedRow();
    
    if (selectedRow != -1) { 
        String delete = Book.getValueAt(selectedRow, 0).toString();
        
           try {
               ConnectXamppSQL.Delete("books", "number" , "=", delete );
           } catch (Exception ex) {
               Logger.getLogger(frmInventory.class.getName()).log(Level.SEVERE, null, ex);
           }
        
        DefaultTableModel model = (DefaultTableModel) Book.getModel();
        model.removeRow(selectedRow);
javax.swing.JOptionPane.showMessageDialog(null, "Item deleted successfully!");
} else {
    javax.swing.JOptionPane.showMessageDialog(null, "Please select a row first.");
}

  
        
        
    }//GEN-LAST:event_btnDeleteMouseClicked

