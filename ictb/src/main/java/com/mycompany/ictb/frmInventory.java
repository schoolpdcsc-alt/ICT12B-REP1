    private void btnViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnViewMouseClicked
       try{
            var m =(javax.swing.table.DefaultTableModel)Book.getModel();
            m.setRowCount(0);
            List<Map<String,String>> results = ConnectXamppSQL.Read ("books")
                    .get();
                for (Map<String,String> r : results){
                    m.addRow (new Object[] {r.get("number"),r.get("title"), r.get("author")});
                    
                } 
           
                        }  
        
        catch (Exception ex) {
             ex.printStackTrace();
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnViewMouseClicked

